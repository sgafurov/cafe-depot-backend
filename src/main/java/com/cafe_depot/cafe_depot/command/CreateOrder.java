package com.cafe_depot.cafe_depot.command;

import java.util.Date;
import java.util.List;

import javax.validation.constraints.NotBlank;
import javax.validation.constraints.NotEmpty;
import javax.validation.constraints.NotNull;

import com.cafe_depot.cafe_depot.models.CartItemModel;

public class CreateOrder {

    @NotNull
    private Long userId;

    @NotEmpty
    private List<CartItemModel> orderItems;

    @NotBlank
    private Double total;

    public CreateOrder() {
    }

    public CreateOrder(Long userId, List<CartItemModel> orderItems, Double total) {
        this.userId = userId;
        this.orderItems = orderItems;
        this.total = total;
    }

    public Long getUserId() {
        return userId;
    }

    public void setUserId(Long userId) {
        this.userId = userId;
    }

    public List<CartItemModel> getOrderItems() {
        return orderItems;
    }

    public void setOrderItems(List<CartItemModel> orderItems) {
        this.orderItems = orderItems;
    }

    public Double getTotal() {
        return total;
    }

    public void setTotal(Double total) {
        this.total = total;
    }

    @Override
    public String toString() {
        return "CreateOrder [userId=" + userId + ", orderItems=" + orderItems + ", total=" + total + ", orderDate="
                + "]";
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((userId == null) ? 0 : userId.hashCode());
        result = prime * result + ((orderItems == null) ? 0 : orderItems.hashCode());
        result = prime * result + ((total == null) ? 0 : total.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        CreateOrder other = (CreateOrder) obj;
        if (userId == null) {
            if (other.userId != null)
                return false;
        } else if (!userId.equals(other.userId))
            return false;
        if (orderItems == null) {
            if (other.orderItems != null)
                return false;
        } else if (!orderItems.equals(other.orderItems))
            return false;
        if (total == null) {
            if (other.total != null)
                return false;
        } else if (!total.equals(other.total))
            return false;
        return true;
    }

}
